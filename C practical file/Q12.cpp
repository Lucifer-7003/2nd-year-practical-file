// Q12. WAP to print "Aryabhatta College Ajmer" 10 times by using while loop.

#include <iostream>
using namespace std;

int main()
{
    int i = 0;     // Initialize counter variable i to 0
    while (i < 10) // Loop through 10 times
    {
        // Print college name
        cout << "Aryabhatta College Ajmer" << endl;
        // Increment counter variable i by 1
        i++;
    }
    return 0; // End program
}
