# Q25. Write a Python program to import the Math module and show how to use it.

import math

# Example 1: finding square root
num = 25
sqrt_num = math.sqrt(num)
print("Square root of", num, "is", sqrt_num)

# Example 2: finding power of a number
base = 2
power = 3
result = math.pow(base, power)
print(base, "raised to power", power, "is", result)

# Example 3: finding the value of pi
pi = math.pi
print("Value of pi is", pi)
