# Q1. Write a Python program to add two numbers.

# The following lines prompt the user to input two numbers and converts them to float data type
num1 = float(input("Enter the first number: "))
num2 = float(input("Enter the second number: "))

# The sum of the two numbers is calculated and stored in a variable named sum
sum = num1 + num2

# The result is printed with f-string formatting
print(f"The sum of {num1} and {num2} is {sum}")
